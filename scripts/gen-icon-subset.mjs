#!/usr/bin/env node
// Generate a *subset* of iconoir's CSS containing only the icons this dashboard
// actually uses. The full iconoir.css inlines ~1500 icons as SVG data-URIs (~1.96MB)
// and was being shipped whole in the render-blocking bundle. We use ~60 icons.
//
// Re-run whenever you add/remove an `iconoir-*` class (or a nav-bar constant icon):
//   node scripts/gen-icon-subset.mjs
// It scans src/ for every `iconoir-<name>` token plus the unprefixed `icon: "<name>"`
// values in nav-bar.constants.ts (used as `'iconoir-' + item.icon`), then copies each
// matching rule VERBATIM from node_modules/iconoir/css/iconoir.css — so rendering is
// byte-identical to the full set, just trimmed.
import { readFileSync, writeFileSync, readdirSync, statSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const SRC = join(root, "src");
const ICONOIR = join(root, "node_modules/iconoir/css/iconoir.css");
// NB: the output filename is deliberately NOT prefixed `iconoir-` — otherwise the
// `@import "./icons-subset.css"` line in style.scss would itself match the scanner.
const OUT = join(root, "src/styles/icons-subset.css");

// --- collect used icon names ------------------------------------------------
const used = new Set();
const walk = (dir) => {
  for (const entry of readdirSync(dir)) {
    const p = join(dir, entry);
    if (statSync(p).isDirectory()) walk(p);
    // Scan source only — NOT .css (the generated subset would match its own header).
    else if (/\.(ts|html|scss)$/.test(entry)) {
      const txt = readFileSync(p, "utf8");
      for (const m of txt.matchAll(/iconoir-([a-z0-9]+(?:-[a-z0-9]+)*)/g)) used.add(m[1]);
      // nav-bar builds class names dynamically: `'iconoir-' + item.icon`
      if (entry === "nav-bar.constants.ts") {
        for (const m of txt.matchAll(/icon:\s*"([a-z0-9-]+)"/g)) used.add(m[1]);
      }
    }
  }
};
walk(SRC);

// --- pull the matching rules from the full iconoir.css ----------------------
const full = readFileSync(ICONOIR, "utf8");
// Shared base rule(s): everything from the first `*[class` selector up to the first
// per-icon `.iconoir-` rule.
const baseStart = full.indexOf("*[class");
const firstIcon = full.indexOf(".iconoir-", baseStart);
const base = full.slice(baseStart, firstIcon).trim();

const names = [...used].sort();
const rules = [];
const missing = [];
for (const name of names) {
  // SVG path data never contains `}`, so [^}]* is a safe rule terminator.
  const re = new RegExp("\\.iconoir-" + name.replace(/[-]/g, "\\-") + "::before\\{[^}]*\\}");
  const m = full.match(re);
  if (m) rules.push(m[0]);
  else missing.push(name);
}

if (missing.length) {
  console.error("WARNING: icons used but NOT found in iconoir.css:", missing.join(", "));
}

const header =
  `/* GENERATED by scripts/gen-icon-subset.mjs — DO NOT EDIT BY HAND.\n` +
  ` * Subset of iconoir (${names.length} icons in use, of ~1500). Re-run the script\n` +
  ` * after changing any iconoir-* usage. Rules copied verbatim from iconoir@${pkgVer()}. */\n`;

writeFileSync(OUT, header + base + "\n" + rules.join("\n") + "\n");
console.log(`Wrote ${OUT}: ${names.length} icons, ${(header.length + base.length + rules.join("\n").length) / 1024 | 0}KB`);

function pkgVer() {
  try {
    return JSON.parse(readFileSync(join(root, "node_modules/iconoir/package.json"), "utf8")).version;
  } catch { return "?"; }
}
